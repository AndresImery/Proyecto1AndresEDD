    /**
     * Carga las listas de usuarios y relaciones al jFrame
     */
    public void loadLists() {
        List<Relation> relationList = this.v1.getGrafo().getRelations();
        Nodo<Relation> pointer = relationList.getHead();
        String string;
        while (pointer != null) {
            string = pointer.getElement().getUser1().getUsername() + " lleva con " + pointer.getElement().getUser2().getUsername() + " " + pointer.getElement().getTime() + " a√±os";
            listRelations.add(string);
            
            pointer = pointer.getNext();
        }
        
        List<User> userList = this.v1.getGrafo().getUsers();
        Nodo<User> pointer2 = userList.getHead();
        String string2;
        while (pointer2 != null) {
            string2 = "ID: " + pointer2.getElement().getId() + ", Name: " + pointer2.getElement().getUsername();
            listUsers.add(string2);
            
            pointer2 = pointer2.getNext();
        }
    }
